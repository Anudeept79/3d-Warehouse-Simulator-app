/**
 * DATA SOURCE SEAM — the integration point for connecting WarePro to real systems.
 *
 * The renderer, twin map, inspector, detection overlays and dashboards all read from
 * shared state (`state.js: trackables / stats / incident`) and from the entity arrays.
 * They do NOT care where that data comes from. A data source's only job is to keep
 * that state in sync each tick.
 *
 * Demo mode uses SimulatedDataSource (the built-in physics/behavior sim).
 * To drive the twin from production systems (WMS, RTLS, camera AI), implement the
 * same interface in a LiveDataSource — a skeleton is provided below.
 *
 * Interface:
 *   start()            — connect / spawn entities (called once at boot)
 *   update(dt, time)   — advance one tick; dt is sim-scaled seconds
 *   stop()             — disconnect / cleanup
 */
import { buildTrucks, updateTrucks } from '../world/trucks.js';
import { buildForklifts, updateForklifts } from '../world/forklifts.js';
import { buildWorkers, workers, updateWorker } from '../world/workers.js';
import { updatePallets } from '../world/pallets.js';
import { updateScripted } from '../world/security.js';

/** Demo data source: everything is generated by the local behavior simulation. */
export class SimulatedDataSource {
  start() {
    buildTrucks();
    buildForklifts();
    buildWorkers();
  }
  update(dt, time) {
    updateTrucks(dt);
    updateForklifts(dt, time);
    for (const w of workers) updateWorker(w, dt, time);
    updateScripted(dt, time);
    updatePallets(dt);
  }
  stop() {}
}

